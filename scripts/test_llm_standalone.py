def test_chat_client():
    """Test ChatClient initialization and basic usage."""
    print("\n" + "=" * 70)
    print("TEST 4: ChatClient Initialization")
    print("=" * 70)

    try:
        from openai import OpenAI
    except ImportError:
        print("â­ï¸  Skipped (OpenAI package not installed)")
        return None

    api_key = os.getenv('OPENAI_API_KEY')
    if not api_key:
        print("â­ï¸  Skipped (No API key configured)")
        return None

    try:
        client = OpenAI(api_key=api_key)
        print("âœ“ OpenAI client initialized")

        # Test a simple completion
        print("\nTesting chat completion...")
        response = client.chat.completions.create(
            model="gpt-3.5-turbo",
            messages=[
                {"role": "user", "content": "Say 'test successful' in exactly 2 words"}
            ],
            max_tokens=10,
            temperature=0.1
        )

        content = response.choices[0].message.content
        print(f"âœ“ Response: {content}")
        print(f"âœ“ Tokens used: {response.usage.total_tokens}")

        print("\nâœ… ChatClient test passed!\n")
        return True

    except Exception as e:
        print(f"âœ— ChatClient test failed: {e}")
        return False


