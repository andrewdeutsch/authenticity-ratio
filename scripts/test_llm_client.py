def main():
    parser = argparse.ArgumentParser(description='Test OpenAI ChatClient and LLM provenance')
    parser.add_argument('--text', type=str, help='Custom text to summarize')
    parser.add_argument('--skip-api', action='store_true', help='Skip tests requiring API calls')
    args = parser.parse_args()

    print("\n" + "=" * 70)
    print("ğŸ§ª OpenAI ChatClient Test Suite")
    print("=" * 70)
    print()

    # Track results
    results = {}

    # Test 1: Provenance helper (no API required)
    try:
        test_provenance_helper()
        results['provenance'] = True
    except Exception as e:
        print(f"âœ— Provenance test failed: {e}\n")
        results['provenance'] = False

    if args.skip_api:
        print("â­ï¸  Skipping API tests (--skip-api flag set)")
        return

    # Test 2: Initialize client
    client = None
    try:
        init_success = test_chat_client_init()
        results['init'] = init_success

        if init_success:
            client = ChatClient()
    except Exception as e:
        print(f"âœ— Initialization test failed: {e}\n")
        results['init'] = False

    if not client:
        print("\nâš ï¸  Cannot proceed with API tests - ChatClient not initialized")
        print("   Make sure OPENAI_API_KEY is set in your environment or .env file")
        print_summary(results)
        sys.exit(1)

    # Test 3: Chat completion
    try:
        results['chat'] = test_chat_completion(client)
    except Exception as e:
        print(f"âœ— Chat test failed: {e}\n")
        results['chat'] = False

    # Test 4: Summarization
    try:
        results['summarize'] = test_summarization(client, args.text)
    except Exception as e:
        print(f"âœ— Summarization test failed: {e}\n")
        results['summarize'] = False

    # Test 5: Report integration
    try:
        results['integration'] = test_llm_summarize_integration()
    except Exception as e:
        print(f"âœ— Integration test failed: {e}\n")
        results['integration'] = False

    # Summary
    print_summary(results)


def print_summary(results):
    """Print test summary."""
    print("\n" + "=" * 70)
    print("ğŸ“Š TEST SUMMARY")
    print("=" * 70)

    passed = sum(1 for v in results.values() if v is True)
    failed = sum(1 for v in results.values() if v is False)
    skipped = sum(1 for v in results.values() if v is None)
    total = len(results)

    for test_name, result in results.items():
        status = "âœ… PASS" if result is True else ("âŒ FAIL" if result is False else "â­ï¸  SKIP")
        print(f"{status:12} {test_name}")

    print("-" * 70)
    print(f"Total: {total} | Passed: {passed} | Failed: {failed} | Skipped: {skipped}")
    print("=" * 70)

    if failed > 0:
        print("\nâŒ Some tests failed. See output above for details.")
        sys.exit(1)
    elif passed > 0:
        print("\nâœ… All tests passed!")
    else:
        print("\nâš ï¸  No tests were run.")


if __name__ == '__main__':
    main()
